// A directed graph is a graph where all the edges are directed from one vertex to another, i.e,
// there will be a directed edge. It contains an ordered pair of vertices.
//  It implies each edge is represented by a directed pair <u, v>. Therefore, <u, v> and <v, u> represent two different edges.




// 2. bi-directional graph / undirected graph
// edged point to both of the vertex  1<->2

// An undirected graph is a graph where edges are bidirectional, with no direction associated with them, i.e,
//  there will be an undirected edge.
//  In an undirected graph, the pair of vertices representing any edge is unordered.
// Thus, the pairs (u, v) and (v, u) represent the same edge.





